    public PluginPanel(String name, String url, String desc, String id, PluginConfigPanel panel, boolean selected) {
        _name = name;
        _url = url;
        _desc = desc;
        _id = id;
        _panel = panel;
        _selected = selected;
        
        initComponents();
        
        if (_selected) {
            this.setBorder(BorderFactory.createLineBorder(Color.BLACK, 5));
        } else {
            this.setBorder(BorderFactory.createLineBorder(Color.LIGHT_GRAY, 5));
        }
        
        Plugin plugin = AbbozzaServer.getPluginManager().getPlugin(id);
        
        String text = _desc + "\n\n";
        
        if ( plugin != null ) {
            text = text + AbbozzaLocale.entry("gui.is_installed");
        }
        descPane.setBackground(Color.WHITE);
        descPane.setText(text);

    }

